CREATE TABLE PREREQUISITOS (
    PLAN CHAR(3),
    ASIGNATURA_ID TEXT,
    ASIGNATURA TEXT,
    NIVEL INT,
    PREREQUISITOS TEXT
);

CREATE TABLE NOTAS (
    CODIGO_PLAN CHAR(3), --POSIBLE LLAVE FORANEA
    PLAN TEXT,
    COHORTE TEXT,
    SEDE TEXT,
    RUN VARCHAR(10), --POSIBLE LLAVE FORANEA
    DV CHAR(1), --POSIBLE LLAVE FORANEA
    NOMBRES TEXT,
    APELLIDO_PATERNO TEXT,
    APELLIDO_MATERNO TEXT,
    NUMERO_DE_ALUMNO INT,
    PERIODO_ASIGNATURA TEXT,
    FOREIGN KEY(CODIGO_ASIGNATURA) REFERENCES ASIGNATURAS(ASIGNATURA_ID),
    ASIGNATURA TEXT,
    CONVOCATORIA TEXT,
    CALIFICACION TEXT,
    NOTA FLOAT
);

CREATE TABLE PLANES (
    CODIGO_PLAN CHAR(3) PRIMARY KEY,
    FACULTAD TEXT,
    CARRERA TEXT,
    PLAN TEXT,
    JORNADA TEXT,
    SEDE TEXT,
    GRADO TEXT,
    MODALIDAD TEXT,
    INICIO_VIENCIA DATE
);

CREATE TABLE ASIGNATURAS (
    PLAN CHAR(3),
    ASIGANTURA_ID TEXT,
    ASIGNATURA TEXT,
    NIVEL INT,
);

CREATE TABLE ALUMNOS (
    CODIGO_PLAN CHAR(3), --POSIBLE LLAVE FORANEA
    CARRERA TEXT,
    COHORTE TEXT,
    NUMERO_DE_ALUMNO INT,
    BLOQUEO CHAR(1),
    CAUSAL_BLQUEO TEXT,
    PRIMARY KEY RUN VARCHAR(10), --POSIBLE LLAVE COMPUESTA CON DV
    DV CHAR(1), --POSIBLE LLAVE COMPUESTA CON RUN
    NOMBRES TEXT,
    PRIMER_APELLIDO TEXT,
    SEGUNDO_APELLIDO TEXT,
    LOGRO TEXT,
    FECHA_LOGRO VARCHAR(7),
    ULTIMA_CARGA VARCHAR(7) NULL,
);

CREATE TABLE PLANEACION (
    PERIODO VARCHAR(7)
    SEDE TEXT,
    FACULTAD TEXT,
    CODIGO_DEPARTAMENTO CHAR(5)
    DEPARTAMENTO TEXT,
    ID_ASIGNATURA TEXT, --POSIBLE LLAVE FORANEA
    ASIGNATURA TEXT,
    SECCION INT,
    DURACION CHAR(1),
    JORNADA TEXT,
    CUPOS INT,
    INSCRITOS INT,
    DIA TEXT,
    HORA_INICIO TIME,
    HORA_FINAL TIME,
    FECHA_INICIO DATE,
    FECHA_FIN DATE,
    LUGAR TEXT,
    EDIFICIO TEXT,
    PROFESDOR_PRINCIPAL TEXT,
    RUN VARCHAR(10), --POSIBLE LLAVE FORANEA
    NOMBRE_DOCENTE TEXT,
    APELLIDO_DOCENTE TEXT,
    2DO_APELLIDO_DOCENTE TEXT,
    JERARQUIZACION TEXT, 
);

CREATE TABLE DOCENTES_PLANIFICADOS (
    RUN VARCHAR(10) PRIMARY KEY,
    NOMBRE TEXT,
    APELLIDO_PATERNO TEXT,
    TELEFONO INT,
    EMAIL_PERSONAL TEXT,
    EMAIL_INSTITUCIONAL TEXT,
    DEDICACION INT,
    CONTRATO TEXT,
    DIURNO BOOLEAN,
    VESPERTINO BOOLEAN,
    SEDE TEXT,
    CARRERA TEXT,
    GRADO_ACADEMICO TEXT,
    JERARQUIA TEXT,
